def get_question_type(question_number: int) -> str:
    """
    Determine question type based on question number.

    Args:
        question_number: Question number (1-25)

    Returns:
        Question type string for the API (lowercase as expected by API)
    """
    if 1 <= question_number <= 10:
        return "multiple_choice"
    elif 11 <= question_number <= 20:
        return "cloze_select"
    elif 21 <= question_number <= 25:
        return "synonym_completion"
    else:
        raise ValueError(f"Invalid question number: {question_number}")


def get_answer(test_number: int, question_number: int) -> str:
    """
    Get the correct answer for a specific question.

    Args:
        test_number: Test number (1-20)
        question_number: Question number (1-25)

    Returns:
        Correct answer string
    """
    if test_number not in ANSWER_KEYS:
        raise ValueError(f"Invalid test number: {test_number}")

    test_answers = ANSWER_KEYS[test_number]

    if question_number not in test_answers:
        raise ValueError(f"Invalid question number: {question_number}")

    return test_answers[question_number]


